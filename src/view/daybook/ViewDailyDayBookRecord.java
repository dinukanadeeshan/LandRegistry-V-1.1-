    private void reloadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reloadButtonActionPerformed
        dateFormat = DateFormat.getDateInstance(DateFormat.SHORT);
        String dateAr[] = dateFormat.format(calendarCombo.getDate()).split("/");
        String date = dateAr[2] + "/" + dateAr[0] + "/" + dateAr[1];
        System.out.println(date);
        int rowCount = dayBookInfoTable.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            ((DefaultTableModel) dayBookInfoTable.getModel()).removeRow(0);
        }
        try {
            ArrayList<ArrayList<String>> dailyDayBookRecords = DayBookController.getDailyDayBookRecords(date);
            for (int i = 0; i < dailyDayBookRecords.size(); i++) {
                ArrayList<String> dayBookInfo = dailyDayBookRecords.get(i);
                String client = dayBookInfo.get(2) + " - " + dayBookInfo.get(3);
                String notry = dayBookInfo.get(5) + " - " + dayBookInfo.get(6);
                ((DefaultTableModel) dayBookInfoTable.getModel()).addRow(new Object[]{dayBookInfo.get(0), dayBookInfo.get(1), client, dayBookInfo.get(4), notry, dayBookInfo.get(7), dayBookInfo.get(8), dayBookInfo.get(9)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewDailyDayBookRecord.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ViewDailyDayBookRecord.class.getName()).log(Level.SEVERE, null, ex);
        }

        int newRowCount = dayBookInfoTable.getRowCount();
        if (newRowCount <= 0) {
            JLabel messageLabel = new JLabel("No any Day Book Records for - " + date);
            messageLabel.setFont(new Font("High Tower Text", Font.PLAIN, 18));
            JOptionPane.showMessageDialog(this, messageLabel, "", JOptionPane.INFORMATION_MESSAGE);

        }
        printButton.setEnabled(newRowCount > 0);
    }//GEN-LAST:event_reloadButtonActionPerformed

    private void printButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printButtonActionPerformed
        setModal(false);
        JRTableModelDataSource dataSource = new JRTableModelDataSource((DefaultTableModel) dayBookInfoTable.getModel());
        Map<String, Object> param = new HashMap<String, Object>();
        dateFormat = DateFormat.getDateInstance(DateFormat.MEDIUM);
        param.put("Date", dateFormat.format(calendarCombo.getDate()));
        try {
            JasperReport jasperReport = JasperCompileManager.compileReport("./src/reports/DailyDayBookReport.jrxml");
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, param, dataSource);
            JasperViewer jasperViewer = new JasperViewer(jasperPrint, false);
            Container contentPane = jasperViewer.getContentPane();
            JDialog jasperDialog = new JDialog(this, false);
            jasperDialog.setContentPane(contentPane);
            jasperDialog.setTitle("Print Preview");
            jasperDialog.setSize(Toolkit.getDefaultToolkit().getScreenSize());
            jasperDialog.setLocationRelativeTo(this);
            jasperDialog.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(ViewDailyDayBookRecord.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_printButtonActionPerformed

