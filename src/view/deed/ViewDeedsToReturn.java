    private void reloadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reloadButtonActionPerformed
        try {
            int rowCount = deedInfoTable.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                ((DefaultTableModel)deedInfoTable.getModel()).removeRow(0);
            }
            ArrayList<String> dueToReturnDayBookNoList = DayBookController.getDueToReturnDayBookNo();
            for (int i = 0; i < dueToReturnDayBookNoList.size(); i++) {
                String dayBookNo = dueToReturnDayBookNoList.get(i);
                ArrayList<String> deedInfo = DeedController.getDeedInfo(dayBookNo);
                ((DefaultTableModel) deedInfoTable.getModel()).addRow(new Object[]{dayBookNo, deedInfo.get(0), deedInfo.get(4), deedInfo.get(1), deedInfo.get(2), deedInfo.get(3), deedInfo.get(5), deedInfo.get(6) });
            }
            if (deedInfoTable.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "No Records Founds..");           
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ViewDeedsToReturn.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ViewDeedsToReturn.class.getName()).log(Level.SEVERE, null, ex);
        }
         returnButton.setEnabled(deedInfoTable.getRowCount() > 0 ? true : false);
    }//GEN-LAST:event_reloadButtonActionPerformed

