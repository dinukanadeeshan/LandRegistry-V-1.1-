    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        LocalAuthority office = new LocalAuthority(authorityCodeTextField.getText(), officeNameTextField.getText());
        try {
            int res = LocalAuthorityController.updateAuthority(office);
            if (res > 0) {
                JLabel messageLabel = new JLabel("Update Successfull...");
                messageLabel.setFont(new Font("High Tower Text", Font.PLAIN, 18));
                JOptionPane.showMessageDialog(this, messageLabel);
//                initAuthorityCombo();
            } else {
                JLabel messageLabel = new JLabel("Update failed...");
                messageLabel.setFont(new Font("High Tower Text", Font.PLAIN, 18));
                JOptionPane.showMessageDialog(this, messageLabel);
            }

        } catch (SQLException ex) {
            Logger.getLogger(UpdateLocalAuthority.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateLocalAuthority.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (dialog instanceof ViewLocalAuthority) {
            this.dispose();
            ((ViewOffice) dialog).initOfficeInfoTable();
        }
    }//GEN-LAST:event_updateButtonActionPerformed

