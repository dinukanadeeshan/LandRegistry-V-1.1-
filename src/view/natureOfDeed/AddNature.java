    private void regDutyTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regDutyTextActionPerformed


        if (!regDutyText.getText().equals("")) {
            saveButton.doClick();
        } else {
            JLabel lbl = new JLabel("Please Enter Registration Duty!");
            lbl.setForeground(Color.red);
            PopupFactory popupFactory = PopupFactory.getSharedInstance();
            popup = popupFactory.getPopup(this, lbl, 500, 500);
            popup.show();
        }
    }//GEN-LAST:event_regDutyTextActionPerformed

    private void natureCodeTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_natureCodeTextActionPerformed
        if (!natureCodeText.getText().equals("")) {
            try {
                NatureController.searchNature(natureCodeText.getText());
                JLabel lbl = new JLabel("Nature Code exists!");
                lbl.setForeground(Color.red);
                PopupFactory popupFactory = PopupFactory.getSharedInstance();
                popup = popupFactory.getPopup(this, lbl, 500, 500);
                popup.show();

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AddNature.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(AddNature.class.getName()).log(Level.SEVERE, null, ex);
                if (ex.getMessage().contains("empty result set")) {
                    nameText.requestFocus();
                }
            }
        } else {
            JLabel lbl = new JLabel("Please Enter Nature Code!");
            lbl.setForeground(Color.red);
            PopupFactory popupFactory = PopupFactory.getSharedInstance();
            popup = popupFactory.getPopup(this, lbl, 500, 500);
            popup.show();
        }
    }//GEN-LAST:event_natureCodeTextActionPerformed

    private void natureCodeTextKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_natureCodeTextKeyPressed
        try {
            popup.hide();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_natureCodeTextKeyPressed

    private void regDutyTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_regDutyTextKeyReleased
        try {
            String text = regDutyText.getText();
            if (!Validation.validateValueInt(text)) {
                regDutyText.setText(text.substring(0, text.length() - 1));
            }
        } catch (Exception e) {
        }
        setButtonEnability();
    }//GEN-LAST:event_regDutyTextKeyReleased

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        String natureCode = natureCodeText.getText();
        String name = nameText.getText();
        double registrationDuty = Double.parseDouble(regDutyText.getText());
        try {
            int res = NatureController.addNatureOfDeed(new Nature(natureCode, name, registrationDuty));
            if (res > 0) {
                JLabel messageLabel = new JLabel("Saved Successfully...");
                messageLabel.setFont(new Font("High Tower Text", Font.PLAIN, 18));
                JOptionPane.showMessageDialog(this, messageLabel);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddNature.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddNature.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

