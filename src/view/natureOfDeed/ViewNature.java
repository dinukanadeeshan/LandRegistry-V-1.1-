    private void reloadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reloadButtonActionPerformed
        try {
            ArrayList<Nature> natureListOfDeed = NatureController.getNatureListOfDeed();
            int rowCount = viewTable.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                ((DefaultTableModel) viewTable.getModel()).removeRow(0);
            }
            for (int i = 0; i < natureListOfDeed.size(); i++) {
                Nature nature = natureListOfDeed.get(i);
                ((DefaultTableModel) viewTable.getModel()).addRow(new Object[]{nature.getNatureCode(), nature.getName(), numberFormat.format(nature.getRegistrationDuty()).substring(1)});
            }
            if (viewTable.getRowCount() == 0) {
                JLabel messageLabel = new JLabel("No record to display...");
                messageLabel.setFont(new Font("High Tower Text", Font.PLAIN, 18));
                JOptionPane.showMessageDialog(this, messageLabel, "", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ViewNature.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ViewNature.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_reloadButtonActionPerformed

