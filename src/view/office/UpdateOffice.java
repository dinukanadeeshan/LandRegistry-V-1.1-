    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        Office office = new Office(officeCodeTextField.getText(), officeNameTextField.getText());
        try {
            int res = OfficeController.updateOffice(office);
            if (res > 0) {
                JLabel messageLabel = new JLabel("Update successfull...");
                messageLabel.setFont(new Font("High Tower Text", Font.PLAIN, 18));
                JOptionPane.showMessageDialog(this, messageLabel);

                initOfficeCombo();
            } else {
                JLabel messageLabel = new JLabel("Update failed...");
                messageLabel.setFont(new Font("High Tower Text", Font.PLAIN, 18));
                JOptionPane.showMessageDialog(this, messageLabel);

            }
            
        } catch (SQLException ex) {
            Logger.getLogger(UpdateOffice.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateOffice.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (dialog instanceof ViewOffice) {
            this.dispose();
            ((ViewOffice)dialog).initOfficeInfoTable();
        }
    }//GEN-LAST:event_updateButtonActionPerformed

