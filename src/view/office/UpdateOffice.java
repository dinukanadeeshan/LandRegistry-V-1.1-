    public  void initOfficeCombo() throws ClassNotFoundException, SQLException {
        officeComboBox.removeAllItems();
        ArrayList<Office> officeList = OfficeController.getOffices();
        for (Office office : officeList) {
            officeComboBox.addItem(office.getOfficeCode()+" - "+office.getName());
        }
    }
