    public void initOfficeInfoTable() {
        try {
            int rowCount = officeInfoTable.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                ((DefaultTableModel) officeInfoTable.getModel()).removeRow(0);
            }
            ArrayList<Office> officeList = OfficeController.getOffices();
            for (Office office : officeList) {
                ((DefaultTableModel) officeInfoTable.getModel()).addRow(new Object[]{office.getOfficeCode(), office.getName()});
            }


        } catch (SQLException ex) {
            Logger.getLogger(ViewOffice.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ViewOffice.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
