    private void reloadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reloadButtonActionPerformed
        try { 
            int rowCount = villageInfoTable.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                ((DefaultTableModel)villageInfoTable.getModel()).removeRow(0);
            }
            ArrayList<Village> villages = VillageController.getVillages();
            for (int i = 0; i < villages.size(); i++) {
                Village village = villages.get(i);
                LocalAuthority localAuthorityOfVillage = LocalAuthorityController.getLocalAuthorityOfVillage(village.getVillageCode());
                ((DefaultTableModel) villageInfoTable.getModel()).addRow(new Object[]{village.getVillageCode(), village.getName(), localAuthorityOfVillage.getName()});
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DeleteVillage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(DeleteVillage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_reloadButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int selectedRow = villageInfoTable.getSelectedRow();
        if (selectedRow >= 0) {
            String villageCode = (String) villageInfoTable.getValueAt(selectedRow, 0);
            try {
                int res = VillageController.deleteVillage(villageCode);
                if (res > 0) {
                    JOptionPane.showMessageDialog(this, "Deleted Successfully...");
                    reloadButton.doClick();
                }
            } catch (SQLException ex) {
                Logger.getLogger(DeleteVillage.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(DeleteVillage.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Select an row!!","",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

