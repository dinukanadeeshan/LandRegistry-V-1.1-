    private void initLocalAuthorityCombo() {
        try {
            localAuthorityComboBox.removeAllItems();
            ArrayList<String> localAuthorityList = LocalAuthorityController.getLocalAuthorityList();
            for (int i = 0; i < localAuthorityList.size(); i++) {
                String localAuthority = localAuthorityList.get(i);
                localAuthorityComboBox.addItem(localAuthority);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddVillage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddVillage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
