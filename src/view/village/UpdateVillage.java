    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        try {
            String authorityCode = LocalAuthorityController.getLocalAuthorityCode((String) localAuthorityComboBox.getSelectedItem());
            Village village = new Village(villageCodeTextField.getText(), authorityCode, nameText.getText());

            int res = VillageController.updateVillage(village);
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Update successfully..");
            }
            if (parent instanceof ViewVillage) {
                this.dispose();
                ((ViewVillage) parent).reload();
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateVillage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateVillage.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_updateButtonActionPerformed

