    private void reloadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reloadButtonActionPerformed
        int rowCount = villageInfoTable.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            ((DefaultTableModel) villageInfoTable.getModel()).removeRow(0);
        }
        try {
            ArrayList<Village> villages = VillageController.getVillages();
            for (int i = 0; i < villages.size(); i++) {
                Village village = villages.get(i);
                ((DefaultTableModel) villageInfoTable.getModel()).addRow(new Object[]{village.getVillageCode(), LocalAuthorityController.getLocalAuthority(village.getAuthorityCode()).getName(), village.getName()});

            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ViewVillage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ViewVillage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_reloadButtonActionPerformed

