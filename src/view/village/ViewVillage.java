    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        int selectedRow = villageInfoTable.getSelectedRow();
        if (selectedRow >= 0) {
            String villageCode = (String) villageInfoTable.getValueAt(selectedRow, 0);
            new UpdateVillage(this, true, villageCode).show();
        } else {
            JLabel messageLable = new JLabel("Select a Village ...");
            messageLable.setFont(new Font("High Tower Text", Font.PLAIN, 18));
            JOptionPane.showMessageDialog(this, messageLable, "", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_updateButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        int selectedRow = villageInfoTable.getSelectedRow();
        if (selectedRow >= 0) {
            String villageCode = (String) villageInfoTable.getValueAt(selectedRow, 0);
            try {
                VillageController.deleteVillage(villageCode);
            } catch (SQLException ex) {
                Logger.getLogger(ViewVillage.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ViewVillage.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JLabel messageLable = new JLabel("Select a Village ...");
            messageLable.setFont(new Font("High Tower Text", Font.PLAIN, 18));
            JOptionPane.showMessageDialog(this, messageLable, "", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_removeButtonActionPerformed

